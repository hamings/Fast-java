package me.java.datatype;

public class Main {
    public static void main(String[] args) {
        // 자료형(datatype)
        // 원시 자료형 (primitive type): 스택 공간
        // int(4), float(4), double(8), char(2)
        // 1byte = 8bit
        // 참조 자료형(reference type): 힙공간
        // String
        byte b = (byte)100000;  // type casting(명시적 형변환)
        short s = 10;
        int i = 10; //정수의 기본단위
        long l =10; // promotion(자동 형변환)
        float f = 3.14f;
        double d = 3.14; // 실수의 기본단위

    }
}




